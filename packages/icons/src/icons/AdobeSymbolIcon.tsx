import React, { type CSSProperties } from 'react';

export interface AdobeSymbolIconProps {
  size?: string | number;
  className?: string;
  color?: string;
  style?: CSSProperties;
  variant?: 'primary' | 'secondary' | 'success' | 'warning' | 'danger' | 'info' | 'muted' | 'inverse';
}

const AdobeSymbolIcon: React.FC<AdobeSymbolIconProps> = ({
  size = 16,
  className,
  color,
  style,
  variant = 'primary',
  ...props
}) => {
  const styleSafe = (style && typeof style === 'object') ? style : undefined;
  const sizeValue = typeof size === 'number' ? `${size}px` : size;

  // 디자인 토큰 기반 색상 결정
  const getColorValue = () => {
    if (color) return color;
    
    const colorMap = {
      primary: 'var(--color-semantic-component-icon-primary)',
      secondary: 'var(--color-semantic-component-icon-secondary)',
      success: 'var(--color-semantic-component-icon-success)',
      warning: 'var(--color-semantic-component-icon-warning)',
      danger: 'var(--color-semantic-component-icon-danger)',
      info: 'var(--color-semantic-component-icon-info)',
      muted: 'var(--color-semantic-component-icon-muted)',
      inverse: 'var(--color-semantic-component-icon-inverse)'
    };
    
    return colorMap[variant] || colorMap.primary;
  };

  return (
    <svg
      width={sizeValue}
      height={sizeValue}
      className={className}
      style={styleSafe}
      viewBox="0 0 24 24"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      {...props}
    >
      <g clip-path="url(#a)"><path fill={getColorValue()} fillRule="evenodd" d="M8.881 1H0v21.24zm6.25 0H24v21.24zm2.528 21.24L12.006 8.828 8.123 17.97h4.137l1.69 4.27z" clipRule="evenodd"/></g><defs><clipPath id="a"><path fill={getColorValue()} d="M0 0h24v24H0z"/></clipPath></defs>
    </svg>
  );
};

AdobeSymbolIcon.displayName = 'AdobeSymbolIcon';
export default AdobeSymbolIcon;
